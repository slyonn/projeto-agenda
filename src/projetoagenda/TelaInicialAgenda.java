    private void esvaziarMenuItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_esvaziarMenuItemActionPerformed
        if (GerenciadorContato.listaContatosExcluidos.size() == 0) {
            JOptionPane.showMessageDialog(null, "Lixeira já está vazia.");
        } else {
            int opcao = JOptionPane.showConfirmDialog(null, "Remover permanentemente os contatos excluídos?");
            if (opcao == 0) {
                boolean resultado = GerenciadorContato.listaContatosExcluidos.removeAll(listaParametro);
                if (resultado) {
                    JOptionPane.showMessageDialog(null, "Lixeira esvaziada.");
                }
            }
        }
        
        BancoDeDados.salvarContatosExcluidos(GerenciadorContato.listaContatosExcluidos);
    }//GEN-LAST:event_esvaziarMenuItemActionPerformed

