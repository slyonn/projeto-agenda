    public void arquivoContatos(){
        if (BancoDeDados.lerArquivoContatos() == null){
            BancoDeDados.salvarContatos(GerenciadorContato.listaContatos);
            System.out.println("criar");
        } else {
            GerenciadorContato.listaContatos = BancoDeDados.lerArquivoContatos();
            System.out.println("salvar");
        }
    }
    
    public void arquivoContatosExcluidos(){
        if (BancoDeDados.lerArquivoContatosExcluidos() == null){
            BancoDeDados.salvarContatosExcluidos(GerenciadorContato.listaContatosExcluidos);
            System.out.println("criar");
        } else {
            GerenciadorContato.listaContatosExcluidos = BancoDeDados.lerArquivoContatosExcluidos();
            System.out.println("salvar");
        }
    }
    
    public void arquivoContatosParametro(){
        if (BancoDeDados.lerArquivoContatosParametro() == null){
            BancoDeDados.salvarContatosParametro(GerenciadorContato.listaParametro);
            System.out.println("criar");
        } else {
            GerenciadorContato.listaParametro = BancoDeDados.lerArquivoContatosParametro();
            System.out.println("salvar");
        }
    }

